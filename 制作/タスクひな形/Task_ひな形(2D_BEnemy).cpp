	void Object::Received(BEnemy* from_, AttackInfo at_) {
		if (this->unHitTime > 0) { 
			return; //無敵時間中は処理を受けない
		}
		this->unHitTime = 30;
		this->hp -= at_.power;
		if (this->hp <= 0) {
			this->Kill();
		}
		//吹き飛ばされる
		if (this->pos.x > from_->pos.x) {
			this->moveVec = ML::Vec2(+3, -8);
		}
		else {
			this->moveVec = ML::Vec2(-3, -8);
		}
		this->UpdateMotion(Motion::Bound);
	}

