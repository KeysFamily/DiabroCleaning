	//-------------------------------------------------------------------
	//「更新」１フレーム毎に行う処理
	void  Object::UpDate()
	{
		auto ms = ge->mouse->GetState();
		auto inp = ge->in1->GetState();
		this->pos.x = ms.pos.x;
		this->pos.y = ms.pos.y;
		this->select.Addval(ms.wheel);
		if (inp.B1.down)
		{
			this->LoadImg();
		}

	}
	//-------------------------------------------------------------------
	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D draw = OL::setBoxCenter(this->cutSize * this->drawScale);
		draw.Offset(this->pos);
		ML::Box2D src((select.vnow % imgTurn) * this->cutSize.w, (select.vnow / imgTurn) * this->cutSize.h, this->cutSize.w, this->cutSize.h);
		this->img->Draw(draw, src);
	}
	//-------------------------------------------------------------------
	//その他のメソッド
	void Object::LoadImg()
	{
		ifstream ifs("./data/testImgData.txt");
		if (!ifs)
		{
			return;
		}

		string imgPath;
		ifs >> imgPath;
		img.reset();
		img = DG::Image::Create(imgPath);
		ifs >> cutSize.w;
		ifs >> cutSize.h;
		ifs >> imgTurn;
		ifs >> select.vmax;
		ifs >> drawScale;
		select.Setval(0);
	}
